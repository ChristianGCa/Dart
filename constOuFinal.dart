void main() {
  // CONST é uma variavel que não pode
  // ser alterada em tempo de execução
  // e deve ser inicializada na declaração
  // da mesma
  const nome = 'Chris';
  const idade = 50 + 2;
  const lista = ['Daniel', 'João'];

  // FINAL é uma variavel que pode ser
  // alterada em tempo de execução
  final String endereco;
  final DateTime agora = DateTime.now();
}
