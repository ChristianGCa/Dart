void main() {
  String linguagem = 'C';

  switch (linguagem) {
    case 'Dart':
      print("A linguagem é Dart");
      break;

    case 'C#':
      print("A linguagem é C#");
      break;

    case 'Python':
      print("A linguagem é Python");
      break;

    case 'Java':
      print("A linguagem é Java");
      break;

    default:
      print("Linguagem desconhecida");
  }
}
